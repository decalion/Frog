package com.icaballero.proyectofinal.Utils;
/**
 * Clase puntoGeométrico, que guarda la coordena X e Y de un punto en el eje de coordenadas
 * @author Cristian Bautista, Ismael Caballero, Daniel Córdoba
 *
 */
public class PuntoGeometrico {
	public static int x;
	public static int y;
	
	public PuntoGeometrico(int x, int y){
		this.x = x;
		this.y = y;
	}

}
